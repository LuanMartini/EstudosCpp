// Faça um programa em C++ que leia dois números inteiros e exiba o produto entre eles.
// declarar variavel -- pedir numeros -- realizar operação -- imprimir eles

#include <iostream>

int main(){
    int n1, n2, produto;

    std::cout << "Digite o primeiro numero: ";
    std::cin >> n1;

    std::cout << "Digite o segundo numero: ";
    std::cin >> n2;

    produto = n1 * n2;
    std::cout << "A multiplicação dos produtos é: " << produto << "\n";
    return 0;








}